using Mohajer.ClassScheduleProject.DashboardCustomization;
using Mohajer.ClassScheduleProject.DashboardCustomization.Dto;

namespace Mohajer.ClassScheduleProject.Web.Areas.App.Models.CustomizableDashboard
{
    public class CustomizableDashboardViewModel
    {
        public DashboardOutput DashboardOutput { get; }

        public Dashboard UserDashboard { get; }

        public CustomizableDashboardViewModel(
            DashboardOutput dashboardOutput,
            Dashboard userDashboard)
        {
            DashboardOutput = dashboardOutput;
            UserDashboard = userDashboard;
        }
    }
}